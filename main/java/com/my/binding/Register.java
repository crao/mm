package com.my.binding;

public class Register {

	private String profileFor;
	private String firstName;
	private String lastName;
	private String email;
	private int dayob;
	private int monthob;
	private int yearob;
	private String gender;
	private String password;
	private String countryCode;
	private String mobile;
	
	public String getProfileFor() {
		return profileFor;
	}
	public void setProfileFor(String profileFor) {
		this.profileFor = profileFor;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getDayob() {
		return dayob;
	}
	public void setDayob(int dayob) {
		this.dayob = dayob;
	}
	public int getMonthob() {
		return monthob;
	}
	public void setMonthob(int monthob) {
		this.monthob = monthob;
	}
	public int getYearob() {
		return yearob;
	}
	public void setYearob(int yearob) {
		this.yearob = yearob;
	}
	
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}	
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getCountryCode() {
		return countryCode;
	}
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	} 

}
